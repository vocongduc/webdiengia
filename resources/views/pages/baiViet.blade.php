                <h3>Ông tôi kể chuyện rất hay (P.1): Bất kể xã hội thế nào vẫn phải sống thật thà, đó mới là khôn ngoan thực sự</h3>
                <hr>
                <div class="tag">
                <i class="fas fa-tags"></i> <a href="">Bài học cuộc sống</a>
                <i class="fas fa-tags"></i> <a href="">Blog Khởi Nghiệp</a>
                </div>
                <hr>
                <p><strong>Ông nội tôi là người hiền hậu, hay cười, tiếng cười của ông nghe khà khà rất dễ mến. Ông sống một mình trong ngôi nhà thờ họ to lớn ở quê để vừa dưỡng già, vừa chăm sóc hương hỏa các cụ. Ông tự làm lấy mọi việc vì bà nội chúng tôi mất sớm.</strong></p>

                <p>Ông tôi rất dễ tính. Ông mặc gì cũng được, ăn món gì cũng xong và chẳng bao giờ nhận xét món ngon hay dở hoặc quần áo có đẹp hay không, miễn sạch sẽ là được. Nhưng nếu các cháu đến chơi thì tự tay ông vào bếp, và chỉ một loáng sau cơm canh thơm nức ngon lành đã bày lên bàn. Nếu chúng tôi ăn ngon miệng và khoái chí muốn được học hỏi thêm về cách nấu nướng hay chỗ tinh tế của ẩm thực thì lúc ấy ông có thể nói chuyện với chúng tôi cả ngày. Vì từ chuyện ẩm thực, chúng tôi có thể hứng lên hỏi ông đủ thứ chuyện trên trời dưới bể, và ông có thể giải đáp được hầu hết.</p>

                <p>Thích nhất là được ăn với ông ở ngoài sân rộng. Ngoài vườn có mấy cây hoa lan, hoa bưởi thơm ngát. Và được nghe ông nói chuyện.</p>

                <p>Thường trong khi kể chuyện, đôi mắt bình thản của ông thi thoảng lại lóe lên một ánh nhìn vừa tinh anh, vừa hóm hỉnh, và ngay lập tức lại trở về bình thản như cũ. Và thế là tôi, cháu nội yêu quý của ông, chắc mẩm sẽ được nghe những tình tiết hay ho và sâu sắc. Nhưng nếu chúng tôi không hỏi, thì ông lại khà khà cười, tay ve vẩy cây quạt nan để quạt cho các cháu và trìu mến nhìn các cháu ăn cơm.</p>

                <p>Có lần tôi hỏi ông:</p>

                <p>– Ông ơi, ngày xưa các cụ sao ăn nói chừng mực thế ông nhỉ? Sao không thấy các cụ “chém gió” giống như bọn cháu bây giờ?</p>

                <p>– Ý cháu là nói khoác phải không? Vì thời các cụ, các ông còn nhỏ luôn được dạy trước hết về lòng trung thực. Chẳng phải ca dao mình cũng có câu: “Đừng bảo rằng trời không tai, nói đơm nói đặt cậy tài mà chi”, hay là: “Khôn ngoan chẳng lọ thật thà” là gì?</p>
                <div class="text-center">
                    <img class="img-fluid" src="http://phamngocanh.com/wp-content/uploads/2019/07/bai-hoc-cuoc-song-1.jpg">
                </div>
                
                <p>– Nhưng sao cháu thấy trong văn chương hay ca dao tục ngữ các cụ vẫn nói vống lên ạ? Ví dụ: “Lỗ mũi mười tám gánh lông, chồng yêu chồng bảo tơ hồng trời cho”?</p>

                <p>– Hà hà, cái đó gọi là phép thậm xưng cháu ơi. Đấy là một thủ pháp để nhấn mạnh và tạo hình tượng, hay chính là sự nói ngoa, chủ yếu để tạo hài hước. Nhưng ai cũng hiểu đó là thủ pháp văn chương. Nếu ngoài đời có ai đó nói những câu tương tự, thì người ta đều hiểu là nói đùa, nói vui. Còn nếu nói chuyện nghiêm túc mà không thật thì là nói dối rồi, đúng không?</p>

                <p>– Cháu thấy bình thường thì để tạo ấn tượng hay để lấy niềm tin cứ nói vống lên cũng có sao đâu ông? Ai chẳng thế ạ.</p>

                <p>– Cháu ạ. Cái lợi trước mắt cho cá nhân chưa chắc đã đạt được, nhưng cái hại cho xã hội, tức là về lâu dài mọi người đều thiệt hại, thì không phải ai cũng thấy được đâu.</p>

                <p>Ngừng lại một lát ông cười hiền lành. Miệng ông cười, mắt ông cũng nheo cười, nhưng hình như có một ánh chớp vừa thoáng vụt qua.</p>

                <p>– Ngôn ngữ cũng nên có liều lượng, giống như dùng thuốc vậy. Nếu cháu thi thoảng mới dùng thuốc đặc trị, thì thuốc ấy mới có tác dụng. Nếu cháu dùng thường xuyên, thuốc sẽ nhờn. Và cháu sẽ ngày càng phải tăng liều lên.</p>

                <p>Giả sử cháu có một quán cơm, diện tích nhỏ, món ăn đơn giản, thì cháu gọi là “quán cơm bình dân” có phải không? Cứ tạm gọi thế, tên vậy là tương xứng, không hơn không kém. Hoặc cháu gọi quán bằng một tên riêng của cháu cũng được. Nhưng cháu thấy gọi vậy thường quá, cháu sửa sang cơi nới chút ít rồi đặt tên mới là “Trung Hoa đại tửu lâu” để thu hút khách. Quán cơm bên cạnh thấy vậy cũng sốt ruột, cũng sửa sang chút ít rồi đổi tên lại thành “Thế giới ẩm thực”. Nhưng cả “Trung Hoa đại tửu lâu” và “Thế giới ẩm thực” cũng chỉ đi mấy bước chân là hết, món ăn nghèo nàn, dịch vụ sơ sài, giống như bàn chân nhỏ xỏ vào một chiếc giày quá rộng. Khách hàng mới đầu tò mò nên vào đông hơn một chút. Nhưng cũng chỉ dăm bữa nửa tháng là họ lảng dần. Hãy tưởng tượng nếu cháu đi đâu cũng thấy người ta nói vống lên như vậy thì không phải chuyện đùa phải không? Vậy nếu đi theo con đường ấy thì tên quán tiếp theo sẽ phải là gì? Ai sẽ tin đây? Đó là quá trình tăng liều và nhờn ngôn ngữ đấy.</p>

                <p>Ông lại cười khà khà, nhướn mình gắp miếng nem, chấm vào nước chấm rồi thả vào bát của tôi, rồi với tay lấy quạt nan quạt phe phẩy cho tôi ăn được mát mẻ.</p>

                <p>– Ông nghe nói có những quán còn tạo ấn tượng bằng cách đặt tên cho món ăn thức uống của mình thật kêu. Nhưng chẳng qua là thùng rỗng kêu to mà thôi, người tinh ý chỉ cần thử một lần là biết. Còn có người quá đáng hơn, đặt tên món ăn là “Phật nhảy tường”, “La Hán chay”. Ý nói Phật phải thèm đến mức nhảy tường vào để ăn! Phật là bậc tôn quý đã giác ngộ và buông bỏ hết, coi mọi thứ như bùn đất, vậy mà không kìm lòng được trước miếng ăn vậy sao? Họ bất kính với Phật đến thế thì thôi. Tội lớn lắm cháu ạ.</p>
