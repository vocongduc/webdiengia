                <p>Chẳng hiểu ông nói thế vì khiêm tốn hay vì sao, nhưng lòng tôi đang lâng lâng vì những câu chuyện “quê mùa” ấy và cũng vì hoa lan, hoa bưởi ngoài vườn kia đang tỏa hương thơm ngát trong nắng xuân dịu dàng.</p>

